dia = int (input("Ingrese un dia de la semana del 1 al 7: "))

if dia == 1:
    print ("Es Lunes")
elif dia == 2:
    print ("Es Martes")
elif dia == 3:
    print ("Es Miercoles")
elif dia == 4:
    print ("Es Jueves")
elif dia == 5:
    print ("Es Viernes")
elif dia == 6:
    print ("Es Sabado")
elif dia == 7:
    print ("Es Domingo")
elif dia > 7:
    print ("La semana solo tiene 7 dias...")
